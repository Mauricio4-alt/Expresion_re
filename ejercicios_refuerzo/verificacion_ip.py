import re

exp =r"^(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]?[0-9])\."\
     r"(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]?[0-9])\."\
     r"(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]?[0-9])\."\
     r"(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]?[0-9])$"
ip ="01.168.0.1"

result = re.search(exp,ip)
if result:
    print(f"La ip {result.group()} es válida")
else:
    print(f"La ip {ip} no es válida")

